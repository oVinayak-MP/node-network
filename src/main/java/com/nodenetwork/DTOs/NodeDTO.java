package com.nodenetwork.DTOs;

import java.util.Objects;

/**
 * The DTO object to store node information
 *
 *
 * @author vinayak
 */
public class NodeDTO {

    private String name;
    private String type;

    public NodeDTO() {
    }

    public NodeDTO(String nodeName, String type) {
        this.name = nodeName;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String nodeName) {
        this.name = nodeName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 59 * hash + Objects.hashCode(this.name);
        hash = 59 * hash + Objects.hashCode(this.type);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {                      //for junits
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final NodeDTO other = (NodeDTO) obj;
        return true;
    }

}
